package org.railway.com.trainplan.entity.runline;

public class Origin {
	private String origin; //数据源
	private String id; //对应数据源的实体id
	private String name; //对应数据源的实体名称
	
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
